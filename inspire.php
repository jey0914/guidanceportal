    function generateQuote() {
      const quoteText = document.getElementById('quoteText');
      const quoteAuthor = document.getElementById('quoteAuthor');
      const quoteCategory = document.getElementById('quoteCategory');
      const quoteActions = document.getElementById('quoteActions');
      const loadingSpinner = document.getElementById('loadingSpinner');
      const favoriteBtn = document.getElementById('favoriteBtn');
      
      // Show loading
      loadingSpinner.classList.remove('hidden');
      quoteActions.style.opacity = '0';
      
      setTimeout(() => {
        const randomIndex = Math.floor(Math.random() * quotes.length);
        const quote = quotes[randomIndex];
        currentQuoteIndex = randomIndex;
        
        // Update content
        quoteText.textContent = `"${quote.text}"`;
        quoteAuthor.textContent = `â€” ${quote.author}`;
        quoteCategory.textContent = quote.category;
        
        // Update favorite button
        const isFavorite = favorites.some(fav => fav.text === quote.text);
        favoriteBtn.innerHTML = isFavorite ? '<i class="fas fa-heart text-xl"></i>' : '<i class="far fa-heart text-xl"></i>';
        favoriteBtn.classList.toggle('active', isFavorite);
        
        // Hide loading and show actions
        loadingSpinner.classList.add('hidden');
        quoteActions.style.opacity = '1';
        
        // Update counters
        updateCounters();
        updateProgressBar();
        
        // Add animation
        document.getElementById('quoteCard').classList.add('pulse-glow');
        setTimeout(() => {
          document.getElementById('quoteCard').classList.remove('pulse-glow');
        }, 2000);
        
      }, 800);
    }

    function toggleFavorite() {
      const quote = quotes[currentQuoteIndex];
      const favoriteBtn = document.getElementById('favoriteBtn');
      const existingIndex = favorites.findIndex(fav => fav.text === quote.text);
      
      if (existingIndex > -1) {
        favorites.splice(existingIndex, 1);
        favoriteBtn.innerHTML = '<i class="far fa-heart text-xl"></i>';
        favoriteBtn.classList.remove('active');
      } else {
        favorites.push(quote);
        favoriteBtn.innerHTML = '<i class="fas fa-heart text-xl"></i>';
        favoriteBtn.classList.add('active');
      }
      
      localStorage.setItem('favoriteQuotes', JSON.stringify(favorites));
      updateCounters();
    }

    function shareQuote() {
      const quote = quotes[currentQuoteIndex];
      if (navigator.share) {
        navigator.share({
          title: 'Inspiring Quote',
          text: `"${quote.text}" â€” ${quote.author}`,
          url: window.location.href
        });
      } else {
        copyQuote();
      }
    }

    function copyQuote() {
      const quote = quotes[currentQuoteIndex];
      const text = `"${quote.text}" â€” ${quote.author}`;
      navigator.clipboard.writeText(text).then(() => {
        showNotification('Quote copied to clipboard! ðŸ“‹');
      });
    }

    function toggleAutoPlay() {
      const btn = document.getElementById('autoPlayBtn');
      autoPlay = !autoPlay;
      
      if (autoPlay) {
        btn.innerHTML = '<i class="fas fa-pause mr-2"></i>Auto Playing';
        btn.classList.add('bg-green-500/20');
        autoPlayInterval = setInterval(generateQuote, 8000);
        startProgressAnimation();
      } else {
        btn.innerHTML = '<i class="fas fa-play mr-2"></i>Auto Play';
        btn.classList.remove('bg-green-500/20');
        clearInterval(autoPlayInterval);
        document.getElementById('progressBar').style.width = '0%';
      }
    }

    function startProgressAnimation() {
      const progressBar = document.getElementById('progressBar');
      progressBar.style.width = '0%';
      progressBar.style.transition = 'width 8s linear';
      
      setTimeout(() => {
        if (autoPlay) {
          progressBar.style.width = '100%';
        }
      }, 100);
    }

    function updateCounters() {
      document.getElementById('quoteCounter').textContent = `Quote ${currentQuoteIndex + 1} of ${quotes.length}`;
      document.getElementById('favoriteCounter').textContent = `${favorites.length} favorites`;
    }

    function updateProgressBar() {
      const progress = ((currentQuoteIndex + 1) / quotes.length) * 100;
      document.getElementById('progressBar').style.width = progress + '%';
    }

    function showNotification(message) {
      const notification = document.createElement('div');
      notification.className = 'fixed top-4 right-4 bg-white text-gray-800 px-6 py-3 rounded-lg shadow-lg z-50 transform translate-x-full transition-transform duration-300';
